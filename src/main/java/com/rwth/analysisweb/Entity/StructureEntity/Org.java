package com.rwth.analysisweb.Entity.StructureEntity;

public class Org {
    private String name;
    private String uni;
    private String dep;
    private String url_uni;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUni() {
        return uni;
    }

    public void setUni(String uni) {
        this.uni = uni;
    }

    public String getDep() {
        return dep;
    }

    public void setDep(String dep) {
        this.dep = dep;
    }

    public String getUrl_uni() {
        return url_uni;
    }

    public void setUrl_uni(String url_uni) {
        this.url_uni = url_uni;
    }
}
